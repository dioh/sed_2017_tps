function f=f(x,t)
BankersNW=x(1);
ConsNW=x(2);
DepCons=x(3);
DepInv=x(4);
InvNW=x(5);
Loans=x(6);
Reserves=x(7);
Workers=x(8);
WorkersNW=x(9);
IPCValue=0.1;
bfValue=0.1;
rLValue=0.04;
tauBC=1;
CPIValue=0.5;
tauL=7;
tauMC=0.5;
tauMI=0.25;
tauR=9;
tauWC=0.08;
wSValue=0.7;
YIValue=(DepInv)/((tauMI));
YCValue=(DepCons)/((tauMC));
Repay=(Loans)/((tauR));
Money=DepCons+DepInv+Workers+BankersNW;
ConsW=(Workers)/((tauWC));
ConsB=(BankersNW)/((tauBC));
Lend=(DepCons)/((tauL));
Debt=Loans;
Interest=(rLValue)*(Loans);
Output=YCValue+YIValue;
WagesI=(YIValue)*(wSValue);
WagesC=(YCValue)*(wSValue);
IntFee=(Interest)*(bfValue);
PiIG=YIValue-(WagesI);
velocityOfMoney=(Output)/((Money));
PiCG=YCValue-(WagesC);
ConstantBuy=(PiCG)*(CPIValue);
InvBuy=(PiIG)*(IPCValue);
f(1)=IntFee-(ConsB);
f(2)=Interest+InvBuy+ConsW+ConsB-(IntFee+WagesC+ConstantBuy);
f(3)=Interest+Repay+InvBuy+ConsW+ConsB-(Lend+IntFee+WagesC+ConstantBuy);
f(4)=Lend+ConstantBuy-(Interest+Repay+WagesI+InvBuy);
f(5)=ConstantBuy-(Interest+WagesI+InvBuy);
f(6)=Lend-(Repay);
f(7)=0;
f(8)=WagesC+WagesI-(ConsW);
f(9)=WagesC+WagesI-(ConsW);
endfunction;

x0(1)=-0;
