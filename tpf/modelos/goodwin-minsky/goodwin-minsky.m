function f=f(x,t)
D=x(1);
K=x(2);
N=x(3);
a=x(4);
w_R=x(5);
alpha=0.025;
v=2;
r_L=0.04;
pi_Z=0.04;
pi_S=10;
lambda_Z=0.6;
lambda_S=10;
delta_KReal=0.1;
beta=0.015;
Pi=0;
Y=(K)/((v));
Int=(r_L)*(D);
d_r=(D)/((Y));
L=(Y)/((a));
W=(w_R)*(L);
lambda=(L)/((N));
Pi_GReal=Y-(W);
omega=(W)/((Y));
w_fn=(lambda_S)*(lambda-(lambda_Z));
Pi_N=(Pi_GReal)-(Int);
pi_r=(Pi_N)/((K));
I_fnReal=(pi_r-(pi_Z))*(pi_S);
I_G=(I_fnReal)*(Y);
I_NReal=I_G-((K)*(delta_KReal));
f(1)=I_G-(Pi);
f(2)=I_NReal;
f(3)=(N)*(beta);
f(4)=(a)*(alpha);
f(5)=(w_fn)*(w_R);
endfunction;

